    <h2>Microbiological analysis</h2>
    <p>We are able to analyse many types of samples including environmental, waste and drinking waters for the presence and extent of pathogenic microorganisms.</p>  
    <h3>Routine testing includes:</h3>
    <ul class="list">
    <li>Enumeration of total coliforms</li>
    <li>Enumeratoion of E. Coli</li>
    <li>Total colont counts at 37&deg;C and 22&deg;C</li>
    </ul>
    <h3>Additional testing includes:</h3>
    <ul class="list">
        <li>Total coliform count for soil samples</li>
        <li>Legionella identification</li>
    </ul>
    <p>We also carry out biochemical oxygen demand (BOD) tests on environmental and waste water samples.</p>
    <p>Results are provided in the form of digital reports sent via email in PDF format once analysis is complete.</p>
