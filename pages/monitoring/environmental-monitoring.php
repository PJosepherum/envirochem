    <h2>Air quality surveys</h2>
    <img class="floatright" src="/images/dust.jpg" height="100" />
    <p>Light scattering technology can be utilised to determine the ambient concentration of airborne particles (PM<sub>10</sub> or PM<sub>2.5</sub>) in the surrounding environment, which can be monitored to a fraction of a microgram per cubic meter, to facilitate reports and alerts of dust levels in the area.    
