    <h1>Real time monitoring</h1>
    <img class="floatleft" src="/images/alert.jpg" height="120" />
    <p>To facilitate the mediation of environmental impact caused by excess noise and vibration during the high impact works, real time monitors can be installed at boundaries between the site and nearby residential areas which will alert the site manager if a predetermined limit is breached</p>
    <p>Survey reports include a summary of all occurrences of excessive noise, vibration or dust particulates.</p>
 
