    <h2>Envirochem are UKAS accredited for sampling and analysis of total inhalable and respirable dusts.</h2>
    <p>We are happy to undertake site visits, sampling and analysis, to meet the needs of our clients. The initial phase of any project is a discussion about the requirements and what is hoped to be achieve from the monitoring.</p>
    <p>The aims of the project will affect the number and type of samples (static or personal, inhalable or respirable) required as well as the duration of sampling and the sample media.</p>

    <h3>Common indications for dust monitoring</h3>
    <ul class="list">
        <li>As part of on-going COSHH assessments</li>
        <li>To address a specific concern raised by an employee</li>  
        <li>Before and after modifications to site or equipment</li>  
        <li>During grit blasting of lead and other heavy metals</li>  
    </ul>
    <p>At our laboratory, following gravimetric analysis, we can also undertake further tests on the filters to assess type and concentration of airborne contaminates such as:</p>
    <ul class="list">
        <li>Lead and other heavy metals</li>
        <li>Oil or ink mist</li>
        <li>Fibre type and concentration</li>
        <li>Silica type and concentration</li> 
    </ul>
    
