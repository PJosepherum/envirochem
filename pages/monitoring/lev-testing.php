    <h2>Local Exhaust Ventilation (LEV) checks help control the release of hazardous airborne substances from a process into the workplace environment</h2>
    <p>The aim of LEV checking is to control the emission as close as possible to the point of release, and prevent it from entering the air of the workplace.</p>
    <p>Envirochem routinely undertake LEV checks for many clients on equipment including:</p>
    <ul class="list">
        <li>Fume Cabinets</li>
        <li>Spray booths</li>
        <li>Dust extract systems</li>
    </ul>
    <p>These should be tested 14 monthly or after any modifications, as specified within the Health and Safety Executive's HSE Guidance Notes HS(G)54 and HS(G)37.
    <p>We produce thorough reports detailing duct velocities static pressures and flow rates and make comments on any observed changes in the system reading or their possible causes.</p>
    
