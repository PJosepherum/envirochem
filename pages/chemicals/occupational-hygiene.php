    <h1>Dust monitoring</h1>
    <p>Light scattering technology can be used to determine the ambient concentration of airborne particles, which can be monitored to a fraction of a microgram per cubic meter, to facilitate reports and alerts of dust levels in the area. Optionally, weather sensors can be included to measure wind speed and direction.</p>
    <p>Gravimetric dust sampling is also available, to collect physical samples of airborne particles for analysis of their chemical composition and concentration. Our personal sampling equipment takes samples representative of the wearer's rate and volume of respiration.</p>
    
    <h1>Noise monitoring</h1>
    <img class="floatright" src="/images/noisemonitor.jpg" width="200" />
    <p>Our static sampling monitors, positioned at the boundary of a site, can monitor sound levels and facilitate daily analysis and reporting of the impact created by noise on the surrounding environment.</p>
    <p>The results of which will assist in the management of noise from site activities, allowing compliance with limits and operating conditions stipulated by the local authority. All of our surveying equipment conforms to the Class 1 accuracy standard of IEC 61672.</p>
    
    <h1>Vibration monitoring</h1>
    <img class="floatright" src="/images/vibrok.jpg" width="200" />   
    <p>Our site operatives have access to seismographs which can gather analytical information relating to demolition or high impact site activities, to provide reassurance to local residence.</p>
    <p>Measurements can be taken at intervals or a monitor can be set up semi-permenantly to log data and send an alert in the event of exceeding a predetermined level of vibration.</p>    
      
