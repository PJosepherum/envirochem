    <h2>Environmental, process and waste waters</h2>
    <p>We are able to identify the type and concentration of contaminants within many types of water and effluent samples.</p>
    <p>Reports are available usually in PDF format via email upon completion of analysis.</p>
    <h3>Typical analytes include</h3>
    <ul class="list">
        <li>Metals</li>
        <li>Chemical and biochemical oxygen demand (COD, BOD)</li>
        <li>Suspended solids</li>
        <li>Petroleum hydrocarbons including banding, aromatic and aliphatic split</li>
        <li>Solvents</li>
        <li>Polychlorinated Biphenyls (PCBs)</li>
        <li>Polycyclic aromatic hydrocarbons including speciation (<a href="http://www.environment-agency.gov.uk/business/sectors/119330.aspx" target="_blank">EPA 16 PAHs</a>)</li>
        <li>Ammonia and cyanides</li>
        <li>Microbiological tests</li>
        <li>Phenols</li>
    </ul>
