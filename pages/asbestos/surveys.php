	<h1>Management asbestos surveys</h1>
    <p>This type of survey is non-intrusive and is undertaken during normal occupancy to identify and then manage all reasonably accessible asbestos containing materials.</p>
	<ul class="list">
        <li>Executive summary of identified asbestos containing materials, recommended actions and areas of no access</li>
        <li>Introduction and initial observations</li>
        <li>Areas of no access</li>
        <li>Detailed assessment of each room</li>
        <li>Asbestos fibre identification report</li>
        <li>Asbestos material assessment</li>
        <li>Table of negative samples</li>
        <li>Photographs of positive sample locations</li>
        <li>Annotated site plans showing all sample locations</li>
    </ul>

    <h1>Major refurbishment / pre demolition asbestos surveys</h1>
    <p>This type of survey involves intrusive inspection to locate all asbestos containing materials so that they can be removed and is necessary when a building is to be upgraded, refurbished or demolished. They are generally undertaken in buildings which are vacant and/or unoccupied.</p>
    <ul class="list">
        <li>Executive summary of identified asbestos containing materials, recommended actions and areas of no access</li>
        <li>Introduction and initial observations</li>
        <li>Areas of no access</li>
        <li>Asbestos fibre identification report</li>
        <li>Table of positive samples</li>
        <li>Table of negative samples</li>
        <li>Photographs of positive sample locations</li>
        <li>Annotated site plans showing all sample locations</li>
    </ul> 
    
	<h1 id="reinspection-asbestos-survey" class="clear">Re-inspection surveys</h1>
	<p><a href="http://www.hse.gov.uk/asbestos/regulations.htm" target="_blank">The Control of Asbestos Regulations (CAR) 2012</a> specify that identified asbestos containing materials must be re-inspected every 6 to 12 months but more frequently in high disturbance environments. This re-inspection of the condition of the known asbestos based materials will ensure the asbestos material assessment always contains current information. Also any damage caused by disturbance or any deterioration will be identified and therefore remedial work can be undertaken to reduce the risk to building users.</p>
