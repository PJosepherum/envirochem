	<h2>Envirochem routinely carry out a variety of asbestos surveys all within accordance to <a href="http://www.hse.gov.uk/pubns/books/hsg264.htm" target="_blank">HSG 264</a></h2>
    <p>Envirochem undertake management and major refurbishment/demolition asbestos surveys in accordance with our UKAS accreditation based upon <a href="http://www.hse.gov.uk/pubns/books/hsg264.htm" target="_blank">HSE Guidance Note Asbestos: The survey guide (HSG 264)</a>.  We also undertake re-inspection surveys of buildings which have previously undergone management (previously known as type 2) asbestos surveys.</p>
    <p>The report is tailored in every way to the client's needs and can include supplementary information if provided. Reports are produced at our offices using asbestos survey management software, and are available in special formats if required. A signed digital copy of the report is emailed to the client following quality checking.</p>
    <p>Asbestos surveys are quoted on an individual basis. Please <a href="?p=branches">contact us</a> for a free asbestos survey estimate! Provide as much detail as possible about the scope of your project including the size, age, usage, location and any other relevant information about the property.</p>
